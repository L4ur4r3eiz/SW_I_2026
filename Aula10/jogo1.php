<h2>The Last of Us - Parte I</h2>

<h4>
Premissa Narrativa e Linha do Tempo: Lançado originalmente em 2013,
o jogo se passa vinte anos após o início da infecção causada pelo fungo Cordyceps.
A história acompanha Joel Miller, um contrabandista que recebe a missão de escoltar Ellie,
uma adolescente imune à infecção.
</h4>

<h4>
O Mistério da Imunidade: Ellie é a única pessoa conhecida imune ao fungo.
Joel precisa levá-la até os Vagalumes, grupo rebelde que busca desenvolver uma cura.
</h4>

<h4>
Mecânicas de Sobrevivência e Gameplay: O jogo mistura ação e survival horror.
O jogador precisa administrar poucos recursos, fabricar itens e utilizar furtividade
para sobreviver aos inimigos.
</h4>

<h4>
Estágios dos Infectados:
Corredores são rápidos e agressivos.
Perseguidores atacam escondidos.
Estaladores são cegos e usam ecolocalização através dos estalos.
</h4>

