<h2>Como foi Criado</h2>

<h4>
Origem e Divisão na Naughty Dog: O projeto nasceu em 2009, logo após a finalização de Uncharted 2:
Among Thieves. A Naughty Dog tomou a decisão inédita de dividir o estúdio em duas equipes independentes:
uma continuaria com Uncharted 3, enquanto a outra, liderada pelo diretor Bruce Straley e pelo roteirista
Neil Druckmann, recebeu liberdade total para criar uma nova franquia.
</h4>

<h4>
A Ciência por Trás da Infecção: O conceito central foi inspirado em um episódio do documentário
Planet Earth da BBC. O episódio mostrava o fungo parasita Ophiocordyceps unilateralis,
que infecta formigas e altera seu comportamento. A equipe adaptou essa ideia para humanos,
criando a Infecção Cerebral por Cordyceps.
</h4>

<h4>
Revolução na Captura de Movimento: O estúdio utilizou tecnologia de Performance Capture,
onde os atores Troy Baker e Ashley Johnson gravaram voz e movimentos simultaneamente,
garantindo maior realismo nas expressões e diálogos.
</h4>

<h4>
Mudança de Foco no Gênero: O jogo foi pensado como uma experiência de sobrevivência emocional,
focando na relação entre Joel e Ellie em um mundo destruído.
</h4>

