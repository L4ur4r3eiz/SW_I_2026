<h2>The Last of Us - Parte II</h2>

<h4>
Contexto e Linha do Tempo: Lançado em 2020, o jogo acontece cinco anos após os eventos
do primeiro game. Joel e Ellie vivem na comunidade de Jackson,
mas o relacionamento entre eles está abalado.
</h4>

<h4>
Narrativa Espelhada: Após um acontecimento traumático,
Ellie parte em busca de vingança em Seattle.
O jogo também coloca o jogador no controle de Abby,
mostrando os dois lados do conflito.
</h4>

<h4>
Aprimoramentos de Gameplay: O combate ganhou esquiva, salto e rastejamento.
A inteligência artificial foi melhorada,
fazendo os inimigos reagirem de maneira mais realista.
</h4>

<h4>
Novos Infectados: Foram introduzidos os Trôpegos,
que liberam ácido corrosivo,
e o Rei dos Ratos, uma criatura gigantesca formada pela fusão de vários infectados.
</h4>

