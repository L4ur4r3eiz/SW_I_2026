<h2>Além dos Jogos</h2>

<h4>
O Sucesso da Série:
A adaptação da HBO estrelada por Pedro Pascal e Bella Ramsey
tornou-se um fenômeno mundial de audiência e crítica.
</h4>

<h4>
Trilha Sonora:
Gustavo Santaolalla criou a trilha utilizando instrumentos acústicos
e um estilo melancólico que virou marca registrada da franquia.
</h4>

<h4>
HQ Oficial:
A franquia ganhou a HQ The Last of Us: American Dreams,
que mostra acontecimentos anteriores ao primeiro jogo.
</h4>

<h4>
Impacto Comercial:
A franquia vendeu milhões de cópias no mundo inteiro
e se tornou uma das maiores marcas da PlayStation.
</h4>

