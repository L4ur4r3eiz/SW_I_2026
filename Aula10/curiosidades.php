<h2>Curiosidades Tecnológicas e Segredos</h2>

<h4>
A Engenharia de Som dos Estaladores:
Os sons dos Clickers foram criados pela atriz Misty Lee sem efeitos digitais,
utilizando técnicas vocais reais.
</h4>

<h4>
Minigame do Violão:
Na Parte II, os dedos dos personagens se posicionam corretamente
nas cordas e acordes do violão.
</h4>

<h4>
Participações na Série:
Ashley Johnson e Troy Baker,
atores dos jogos,
participaram da adaptação da HBO em papéis especiais.
</h4>

<h4>
Referências Internas:
Os cenários possuem referências escondidas a Uncharted,
PlayStation 3 e outros jogos da Naughty Dog.
</h4>

